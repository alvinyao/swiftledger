package com.higgschain.trust.common.utils;

import java.util.Collection;

/**
 * The type Collection bean.
 *
 * @author ShenTeng
 */
public class CollectionBean {

    private Collection c;

    /**
     * Instantiates a new Collection bean.
     *
     * @param c the c
     */
    public CollectionBean(Collection c) {
        this.c = c;
    }

    /**
     * Gets c.
     *
     * @return the c
     */
    public Collection getC() {
        return c;
    }

    /**
     * Sets c.
     *
     * @param c the c
     */
    public void setC(Collection c) {
        this.c = c;
    }
}
